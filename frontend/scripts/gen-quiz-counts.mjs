#!/usr/bin/env node
/**
 * @file  frontend/scripts/gen-quiz-counts.mjs
 * @domain  quiz
 * @layer  script
 * @related frontend/src/data/quiz/types.ts, frontend/src/data/quiz/question-counts.ts
 *
 * 각 분야 .ts 파일에서 문항별 difficulty를 텍스트 파싱하여
 * frontend/src/data/quiz/question-counts.ts를 생성(덮어쓰기)한다.
 *
 * .mjs는 TypeScript를 직접 import할 수 없으므로
 * readFileSync + 정규식으로 텍스트 파싱 (check-quiz-content.mjs 동일 전략).
 *
 * 멱등성: 동일 데이터면 byte-identical 출력 (분야 순서 canonical 고정).
 *
 * exit
 *   0: 생성 완료
 *   2: I/O 오류
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = fileURLToPath(import.meta.url);
const ROOT = resolve(HERE, '..', '..'); // frontend/
const QUIZ_DIR = resolve(ROOT, 'src', 'data', 'quiz');
const OUT_FILE = resolve(QUIZ_DIR, 'question-counts.ts');

/**
 * 분야 canonical 순서 — 멱등 출력 보장을 위해 고정.
 * gen-quiz-counts.mjs 재실행 시 항상 동일한 순서로 출력한다.
 */
const CATEGORY_FILES = [
  { file: 'data-structure.ts', category: 'DATA_STRUCTURE' },
  { file: 'algorithm.ts', category: 'ALGORITHM' },
  { file: 'network.ts', category: 'NETWORK' },
  { file: 'os.ts', category: 'OS' },
  { file: 'database.ts', category: 'DATABASE' },
  { file: 'computer-architecture.ts', category: 'COMPUTER_ARCHITECTURE' },
  { file: 'design-pattern.ts', category: 'DESIGN_PATTERN' },
  { file: 'web.ts', category: 'WEB' },
  { file: 'security.ts', category: 'SECURITY' },
  { file: 'ai.ts', category: 'AI' },
];

// ──────────────────────────────────────────────────────────────────
// Parsing helpers (check-quiz-content.mjs 동일 전략)
// ──────────────────────────────────────────────────────────────────

/**
 * `difficulty: 'EASY'` 또는 `difficulty: "EASY"` 형태의 값을 추출한다.
 *
 * @param {string} block 문항 블록 텍스트
 * @returns {string|null}
 */
function matchDifficulty(block) {
  const m = block.match(/\bdifficulty:\s*(?:'([^']+)'|"([^"]+)")/);
  return m ? (m[1] ?? m[2]) : null;
}

/**
 * 단일 .ts 소스 텍스트에서 각 문항의 difficulty를 파싱한다.
 * `id: '...'` 위치를 기준으로 각 문항 블록을 잘라 difficulty를 추출한다.
 *
 * @param {string} raw .ts 파일 원문
 * @returns {string[]} difficulty 배열 (null 제거됨)
 */
function parseDifficulties(raw) {
  const idRe = /\bid:\s*'([^']*)'/g;
  const starts = [];
  let m;
  while ((m = idRe.exec(raw)) !== null) {
    starts.push(m.index);
  }
  return starts
    .map((start, i) => {
      const block = raw.slice(start, starts[i + 1] ?? raw.length);
      return matchDifficulty(block);
    })
    .filter(Boolean);
}

/**
 * 분야 파일을 읽어 난이도별 카운트를 반환한다.
 *
 * @param {string} filePath 절대 경로
 * @returns {{ EASY: number, MEDIUM: number, HARD: number }}
 */
function countDifficulties(filePath) {
  const raw = readFileSync(filePath, 'utf-8');
  const difficulties = parseDifficulties(raw);
  return {
    EASY: difficulties.filter((d) => d === 'EASY').length,
    MEDIUM: difficulties.filter((d) => d === 'MEDIUM').length,
    HARD: difficulties.filter((d) => d === 'HARD').length,
  };
}

// ──────────────────────────────────────────────────────────────────
// Code generation
// ──────────────────────────────────────────────────────────────────

/**
 * QUESTION_COUNTS TypeScript 소스 문자열을 생성한다.
 * 멱등성: 동일 entries → byte-identical 출력.
 *
 * @param {Array<{ category: string, counts: { EASY: number, MEDIUM: number, HARD: number } }>} entries
 * @returns {string}
 */
function generateSource(entries) {
  const lines = [
    '// AUTO-GENERATED by frontend/scripts/gen-quiz-counts.mjs — DO NOT EDIT.',
    '/**',
    ' * @file 분야별 난이도 문항 수 (auto-generated)',
    ' * @domain quiz',
    ' * @layer data',
    ' * @related frontend/scripts/gen-quiz-counts.mjs, src/data/quiz/types.ts, src/data/quiz/counts.ts',
    ' */',
    "import { QuizCategory } from './types';",
    "import type { QuizDifficulty } from './types';",
    '',
    '/** 분야별 난이도 문항 수 (gen-quiz-counts.mjs 자동 생성). */',
    'export const QUESTION_COUNTS = {',
  ];

  for (const { category, counts } of entries) {
    lines.push(
      `  [QuizCategory.${category}]: { EASY: ${counts.EASY}, MEDIUM: ${counts.MEDIUM}, HARD: ${counts.HARD} },`,
    );
  }

  lines.push(
    '} as const satisfies Record<QuizCategory, Record<QuizDifficulty, number>>;',
    '',
  );

  return lines.join('\n');
}

// ──────────────────────────────────────────────────────────────────
// Main
// ──────────────────────────────────────────────────────────────────

try {
  const entries = [];
  for (const { file, category } of CATEGORY_FILES) {
    const abs = resolve(QUIZ_DIR, file);
    if (!existsSync(abs)) {
      console.error(`[FAIL] 파일 없음: ${file}`);
      process.exit(2);
    }
    const counts = countDifficulties(abs);
    entries.push({ category, counts });
    const total = counts.EASY + counts.MEDIUM + counts.HARD;
    console.log(
      `[INFO] ${category}: EASY=${counts.EASY} MEDIUM=${counts.MEDIUM} HARD=${counts.HARD} total=${total}`,
    );
  }

  const source = generateSource(entries);
  writeFileSync(OUT_FILE, source, 'utf-8');
  console.log(
    `[OK]   생성 완료 → ${OUT_FILE.replace(ROOT + '/', 'frontend/')}`,
  );
  process.exit(0);
} catch (err) {
  console.error(`[FAIL] I/O 오류: ${err.message}`);
  process.exit(2);
}
